package com.so.dal.core.repository.customDto;

/**
 *
 * @author peter
 */
public class PathsToLogos {

    String name;
    String path;

    public PathsToLogos() {
    }

    public PathsToLogos(String name, String path) {
        this.name = name;
        this.path = path;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

}
